/**
 * Generate syntaxes/engine-commands.json patterns from Phase 1 out/commands YAML.
 * Run from editors/vscode: node scripts/gen-grammar-commands.js
 */
const fs = require("fs");
const path = require("path");

const commandsDir = path.resolve(__dirname, "../../../out/commands");
const outPath = path.resolve(__dirname, "../syntaxes/engine-commands.tmLanguage.json");

if (!fs.existsSync(commandsDir)) {
  console.error(`Missing ${commandsDir}. Run comref-extract first.`);
  process.exit(1);
}

const wordCommands = new Set();
const symbolOps = new Set();

for (const file of fs.readdirSync(commandsDir)) {
  if (!file.endsWith(".yml") && !file.endsWith(".yaml")) continue;
  const text = fs.readFileSync(path.join(commandsDir, file), "utf8");
  const m = text.match(/^name:\s*['"]?([^'"\n]+)['"]?\s*$/m);
  if (!m) continue;
  let name = m[1].trim();
  // YAML may quote: name: '||'
  name = name.replace(/^['"]|['"]$/g, "");
  if (!name) continue;
  if (/^[A-Za-z_][A-Za-z0-9_]*$/.test(name)) {
    wordCommands.add(name);
  } else {
    symbolOps.add(name);
  }
}

const words = [...wordCommands].sort((a, b) => a.localeCompare(b));
const CHUNK = 350;
const wordPatterns = [];
for (let i = 0; i < words.length; i += CHUNK) {
  const chunk = words.slice(i, i + CHUNK);
  const alt = chunk.map(escapeRegExp).join("|");
  wordPatterns.push({
    name: "support.function.engine.sqfts",
    match: `(?i)\\b(${alt})\\b`,
  });
}

const sym = [...symbolOps].sort((a, b) => b.length - a.length); // longer first
const symbolPatterns =
  sym.length === 0
    ? []
    : [
        {
          name: "keyword.operator.sqfts",
          match: `(${sym.map(escapeRegExp).join("|")})`,
        },
      ];

const grammar = {
  scopeName: "source.sqfts.engine-commands",
  patterns: [...symbolPatterns, ...wordPatterns],
  _meta: {
    generated: true,
    wordCommandCount: words.length,
    symbolOpCount: sym.length,
    note: "Generated by scripts/gen-grammar-commands.js — do not edit by hand",
  },
};

fs.writeFileSync(outPath, JSON.stringify(grammar, null, 2) + "\n");
console.log(
  `Wrote ${outPath} (${words.length} word commands, ${sym.length} symbol ops, ${wordPatterns.length} chunks)`
);

function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
