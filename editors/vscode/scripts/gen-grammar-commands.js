/**
 * Generate syntaxes/engine-commands.tmLanguage.json from arma3-wiki command names.
 *
 * Run from editors/vscode (or repo root):
 *   node scripts/gen-grammar-commands.js
 *
 * Requires `cargo` and builds the `dump_command_names` example in sqfts-db.
 */
const { execFileSync } = require("child_process");
const fs = require("fs");
const path = require("path");

const repoRoot = path.resolve(__dirname, "../../..");
const outPath = path.resolve(__dirname, "../syntaxes/engine-commands.tmLanguage.json");

let raw;
try {
  raw = execFileSync(
    "cargo",
    ["run", "-q", "-p", "sqfts-db", "--example", "dump_command_names"],
    { cwd: repoRoot, encoding: "utf8", maxBuffer: 32 * 1024 * 1024 }
  );
} catch (e) {
  console.error("Failed to dump command names via sqfts-db example.");
  console.error(e.stderr || e.message);
  process.exit(1);
}

const data = JSON.parse(raw);
const words = data.words || [];
const sym = data.symbols || [];

const CHUNK = 350;
const wordPatterns = [];
for (let i = 0; i < words.length; i += CHUNK) {
  const chunk = words.slice(i, i + CHUNK);
  const alt = chunk.map(escapeRegExp).join("|");
  wordPatterns.push({
    name: "support.function.engine.sqfts",
    match: `(?i)\\b(${alt})\\b`,
  });
}

const symbolPatterns =
  sym.length === 0
    ? []
    : [
        {
          name: "keyword.operator.sqfts",
          match: `(${sym.map(escapeRegExp).join("|")})`,
        },
      ];

const grammar = {
  scopeName: "source.sqfts.engine-commands",
  patterns: [...symbolPatterns, ...wordPatterns],
  _meta: {
    generated: true,
    wordCommandCount: words.length,
    symbolOpCount: sym.length,
    note: "Generated by scripts/gen-grammar-commands.js from arma3-wiki — do not edit by hand",
  },
};

fs.writeFileSync(outPath, JSON.stringify(grammar, null, 2) + "\n");
console.log(
  `Wrote ${outPath} (${words.length} word commands, ${sym.length} symbol ops, ${wordPatterns.length} chunks)`
);

function escapeRegExp(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}
